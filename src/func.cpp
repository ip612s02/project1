float Summa (float x, float y)
{
	float Sum;
	Sum=x+y;
	return Sum;
}
float Minus (float x, float y)
{
	float Min;
	Min=x-y;
	return Min;
}
float Product (float x, float y)
{
	float Prod;
	Prod=x*y;
	return Prod;
}
float Divit (float x, float y)
{
	float Div;
	Div=x/y;
	return Div;
}
int Fact(int x) 
{ 
    if (x==0)
    return 1;
    return x*Fact(x-1);
}
